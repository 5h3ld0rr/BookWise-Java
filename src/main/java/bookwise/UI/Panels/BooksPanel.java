    private void loadBooksToTable() {
        String searchText = txtSearch.getText();
        boolean isSearching = searchText != null && !searchText.trim().isEmpty() && !searchText.equals("Search...");
        
        javax.swing.SwingUtilities.invokeLater(() -> {
            setCursor(java.awt.Cursor.getPredefinedCursor(java.awt.Cursor.WAIT_CURSOR));
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            model.addRow(new Object[]{null, "Loading data...", "", "", "", null});
        });

        new Thread(() -> {
            Book[] books;
            if (isSearching) {
                books = Book.search(searchText.trim());
            } else {
                books = Book.getAll();
            }

            javax.swing.SwingUtilities.invokeLater(() -> {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                for (Book book : books) {
                    Object[] row = {
                        book.getId(),
                        book.getTitle(),
                        book.getIsbn(),
                        book.getAuthor(),
                        book.getCategory(),
                        book.getAvailableBooks()
                    };
                    model.addRow(row);
                }
                setCursor(java.awt.Cursor.getDefaultCursor());
            });
        }).start();
    }

    private javax.swing.JPopupMenu popupMenu;
