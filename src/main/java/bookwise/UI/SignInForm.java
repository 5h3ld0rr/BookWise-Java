    private void buttonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonLoginActionPerformed
        // TODO add your handling code here:
        // Get input from text fields
        String email = textBoxEmail.getText().trim();        // txtEmail is your JTextField
        String password = new String(textBoxPassword.getPassword()).trim();  // Secure way for JPasswordField

        // Validate required fields
        if (email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(
                this,
                "Please enter all the required fields",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        // Try to parse email as ID (like in your C# code)
        boolean isIdEntered = email.matches("\\d+") && !email.isEmpty();  // Check if it's all digits
        int id = 0;
        if (isIdEntered) {
            try {
                id = Integer.parseInt(email);
            } catch (NumberFormatException e) {
                isIdEntered = false;  // Safety fallback
            }
        }

        try {
            Admin admin = new Admin();
            admin.setPassword(password);

            if (isIdEntered) {
                admin.setId(id);
            } else {
                admin.setEmail(email);
            }

            if (admin.authenticate()) {
                // Login success â€” store user info (use static fields or Session class)
//                Session.userName = admin.getFirstName();
//                Session.userId = admin.getId();
//                Session.userRole = admin.getRole();

                // Close login form with success
                this.dispose();  // Closes the login window

                // Optional: Open main dashboard
                java.awt.EventQueue.invokeLater(() -> new HomeForm(admin.getFirstName(), admin.getId(), admin.getRole()).setVisible(true));
            } else {
                // Clear fields on failed login
                textBoxEmail.setText("");
                textBoxPassword.setText("");

                JOptionPane.showMessageDialog(
                    this,
                    "Invalid credentials",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
                );
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                this,
                "An error occurred: " + ex.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
            ex.printStackTrace();  // For debugging
        }
    }//GEN-LAST:event_buttonLoginActionPerformed

